const STORAGE_KEY = 'visit-tripoli-lang';
const OVERRIDES_KEY = 'visit-tripoli-translation-overrides';

/** API-fetched overrides (source of truth when set). Null = use localStorage fallback. */
let apiOverrides = null;

export function setApiOverrides(data) {
  apiOverrides = data && typeof data === 'object' ? data : {};
}

export function getTranslationOverrides() {
  if (apiOverrides !== null) return apiOverrides;
  try {
    const raw = localStorage.getItem(OVERRIDES_KEY);
    return raw ? JSON.parse(raw) : {};
  } catch {
    return {};
  }
}

export function setTranslationOverride(lang, namespace, key, value) {
  const overrides = getTranslationOverrides();
  if (!overrides[lang]) overrides[lang] = {};
  if (!overrides[lang][namespace]) overrides[lang][namespace] = {};
  if (value === '' || value == null) {
    delete overrides[lang][namespace][key];
    if (Object.keys(overrides[lang][namespace]).length === 0) delete overrides[lang][namespace];
    if (Object.keys(overrides[lang]).length === 0) delete overrides[lang];
  } else {
    overrides[lang][namespace][key] = value;
  }
  localStorage.setItem(OVERRIDES_KEY, JSON.stringify(overrides));
}

/** Replace all overrides (e.g. when API fails, fallback to localStorage) */
export function setTranslationOverrides(data) {
  apiOverrides = null;
  try {
    localStorage.setItem(OVERRIDES_KEY, JSON.stringify(data && typeof data === 'object' ? data : {}));
  } catch (_err) {
    /* localStorage unavailable or quota */
  }
}

export function clearTranslationOverrides() {
  localStorage.removeItem(OVERRIDES_KEY);
}

/**
 * CMS overrides (DB / localStorage) must not pin old marketing copy on the home bento.
 * These keys always use values from this file so deploys update the live site.
 */
function translationOverrideAllowed(namespace, key) {
  if (namespace === 'nav' && key === 'navBrandTagline') return false;
  if (
    namespace === 'home' &&
    (key === 'heroTagline' || key === 'useWebCta' || String(key).startsWith('bento'))
  ) {
    return false;
  }
  return true;
}

/** Get merged translation value (base + overrides) */
export function getTranslation(lang, namespace, key) {
  const base = translations[lang]?.[namespace]?.[key];
  const overrides = getTranslationOverrides();
  const override = overrides[lang]?.[namespace]?.[key];
  if (translationOverrideAllowed(namespace, key) && override !== undefined) return override;
  return base ?? key;
}

export function getStoredLanguage() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && ['en', 'ar', 'fr'].includes(stored)) return stored;
  } catch (_err) {
    /* ignore */
  }
  return 'en';
}

export function setStoredLanguage(code) {
  try {
    localStorage.setItem(STORAGE_KEY, code);
  } catch (_err) {
    /* ignore */
  }
}
